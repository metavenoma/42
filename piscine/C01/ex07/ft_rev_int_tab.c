#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	y;
	int	z;

	x = size - 1;
	y = 0;
	while (y != size / 2)
	{
		z = tab[y];
		tab[y] = tab[x - y];
		tab[x - y] = z;
		y++;
	}
}
