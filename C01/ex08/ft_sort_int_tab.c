void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	while (a < size)
	{
		b = 0;
		while (b < (size - 1))
		{
			if (tab[b] > tab[b + 1])
			{	
				c = tab[b];
				tab[b] = tab[b + 1];
				tab[b + 1] = c;
			}
			b++;
		}
		a++;
	}
}
